<H2>How to buy and print a design?</H2>
<h3>Steps:</h3>
<p class="text-justify">
	1. You can first start by choosing and clicking on the design that you want print on Triplaxis.
	<br>
	2. You can then choose the resolution of the printing. (100 ,200 and 300 microns)
	<br>
	3. Next you would have to choose the selection of colour for your printing.
	<br>
	4. Then you would need to choose the material for your printing.
	<br>
	5. The price estimation will be shown below. (Note: Amount, material type, resolution, price of design and size of design will affect the price.)
	<br>
	6. Finally after deciding on all options, you can proceed to add the items into your cart.
	<br>
	7. After you are done adding items into your cart, you can then proceed to the check out page and start printing your product.
</p>

<h2>How to print your own design?</h2>
<p class="text-justify">
	1. Go to the "Print Your Own Design" page.
	<br>
	2. Upload your own design. (Supports .obj and .stl files only)
	<br>
	3. You can then choose the resolution of the printing. (100 ,200 and 300 microns)
	<br>
	4. Next you would have to choose the selection of colour for your printing.
	<br>
	5. Then you would need to choose the material for your printing.
	<br>
	6. The price estimation will be shown below. (Note: Amount, material type, resolution, price of design and size of design will affect the price.)
	<br>
	7. Finally after deciding on all options, you can proceed to add the items into your cart.
	<br>
	8. After you are done adding items into your cart, you can then proceed to the check out page and start printing your product.
</p>	

<h2>How to sell you design and make money?</h2>
<p class="text-justify">
	1. Go to the "Sell" page.
	<br>
	2. Enter your design's name.
	<br>
	3. Enter selling price.
	<br>
	4. Select your product's category.
	<br>
	5. Fill in the product's description. (Size in volume, use of product and more details.)
	<br>
	6. Upload the images of the actual product. (Front view, top down view ... )
	<br>
	7. Upload your design file. (*NOTE: ONLY .obj & .stl file formats*)
	<br>
	8. Proceed and wait for the money to roll in! 
</p>	

